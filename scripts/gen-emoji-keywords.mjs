#!/usr/bin/env node
// Generates lib/emojiKeywords.generated.ts from the Unicode CLDR annotations
// (en) — the same keyword/annotation set iOS emoji search uses. This backs the
// app's emoji match system (lib/emojiData.ts) so typing "pie" finds 🥧, etc.
//
// Run: node scripts/gen-emoji-keywords.mjs
// Re-run whenever you want to refresh against a newer CLDR release. The output
// is committed; there is no runtime network dependency.

import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const CLDR_URL =
  "https://raw.githubusercontent.com/unicode-org/cldr-json/main/cldr-json/cldr-annotations-full/annotations/en/annotations.json";

// Cap keywords per emoji so the bundled file stays bounded. The CLDR `tts`
// (spoken name) words come first, then the `default` keyword list.
const MAX_KEYWORDS = 12;

// A real emoji must carry a pictographic / flag / keycap code point. Filters
// out CLDR entries for plain punctuation ("{") and bare modifiers.
const HAS_PICTOGRAPHIC =
  /[\p{Extended_Pictographic}⃣\u{1F1E6}-\u{1F1FF}]/u;
// Lone skin-tone modifiers (🏻–🏿) — keep them out of the index.
const LONE_SKIN_TONE = /^[\u{1F3FB}-\u{1F3FF}]$/u;

function tokenize(words) {
  const out = [];
  const seen = new Set();
  for (const phrase of words) {
    for (const tok of String(phrase).toLowerCase().split(/[^a-z0-9]+/)) {
      if (tok.length < 2 || seen.has(tok)) continue;
      seen.add(tok);
      out.push(tok);
      if (out.length >= MAX_KEYWORDS) return out;
    }
  }
  return out;
}

async function main() {
  const res = await fetch(CLDR_URL);
  if (!res.ok) throw new Error(`CLDR fetch failed: HTTP ${res.status}`);
  const data = await res.json();
  const annotations = data.annotations.annotations;

  const rows = [];
  for (const [rawEmoji, info] of Object.entries(annotations)) {
    if (!HAS_PICTOGRAPHIC.test(rawEmoji) || LONE_SKIN_TONE.test(rawEmoji)) continue;
    // CLDR keys text-default single-scalar emoji (✂ ☂ 🏖 ♟ …) WITHOUT the
    // U+FE0F variation selector. Append it so the bundled form is the
    // fully-qualified emoji-presentation string — both so it renders as an
    // emoji (not text) and so it matches the curated list's FE0F forms exactly
    // (otherwise 🏖️ and 🏖 are distinct strings → duplicate picker buttons).
    let emoji = rawEmoji;
    if (
      [...rawEmoji].length === 1 &&
      /\p{Emoji}/u.test(rawEmoji) &&
      !/\p{Emoji_Presentation}/u.test(rawEmoji)
    ) {
      emoji = rawEmoji + "️";
    }
    // tts (canonical name) first so name words lead, then the keyword list.
    const keywords = tokenize([...(info.tts || []), ...(info.default || [])]);
    if (keywords.length === 0) continue;
    rows.push([emoji, keywords.join(" ")]);
  }

  const header = `// AUTO-GENERATED by scripts/gen-emoji-keywords.mjs — do not edit by hand.
// Source: Unicode CLDR annotations (en) — the same emoji keyword set iOS search
// uses. Each entry is [emoji, space-joined keywords]. Consumed by lib/emojiData.ts.
// Regenerate with: node scripts/gen-emoji-keywords.mjs
`;
  const body =
    `export const EMOJI_KEYWORD_INDEX: ReadonlyArray<readonly [string, string]> = [\n` +
    rows
      .map(([e, kw]) => `  [${JSON.stringify(e)}, ${JSON.stringify(kw)}],`)
      .join("\n") +
    `\n];\n`;

  const here = dirname(fileURLToPath(import.meta.url));
  const outPath = join(here, "..", "lib", "emojiKeywords.generated.ts");
  writeFileSync(outPath, header + "\n" + body);
  console.log(`Wrote ${rows.length} emojis → lib/emojiKeywords.generated.ts`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
